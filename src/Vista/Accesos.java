    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        String id, username, email, contraseña;
        String rol, estado;

        id = jTextId.getText();
        username = jTextUsername.getText().trim();
        email = jTextEmail.getText().trim();
        contraseña = jTextPassword.getText().trim();

        rol = (String) jComboBoxRol.getSelectedItem();
        estado = (String) jComboBoxEstado.getSelectedItem();

        if (id.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Selecciona primero un usuario en la tabla para modificarlo.",
                    "Falta seleccion",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (username.isEmpty() || email.isEmpty() || contraseña.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Los campos username, email y contraseña son obligatorios.",
                    "Faltan datos",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        gestorBDO.modificar(id, username, email, contraseña, rol, estado);
        if (jCheckBoxVaciar.isSelected()) {
            System.out.println("Check vaciar seleccionado");
            vaciarCampos();
        } else {
            System.out.println("Check vaciar NO seleccionado");
        }

        refrescarDatos();
    }//GEN-LAST:event_jButtonModificarActionPerformed

