    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int filaSelec, id;
        String username, email, password, rol, estado;
        filaSelec = jTable1.getSelectedRow();
        if (filaSelec < 0 || datos == null || filaSelec >= datos.length) {
            return;
        }
        id = (int) datos[filaSelec][0];
        username = (String) datos[filaSelec][1];
        email = (String) datos[filaSelec][2];
        password = (String) datos[filaSelec][3];
        rol = (String) datos[filaSelec][4];
        estado = (String) datos[filaSelec][5];

        jTextId.setText(String.valueOf(id));
        jTextUsername.setText(username);
        jTextEmail.setText(email);
        jTextPassword.setText(password);
        jComboBoxRol.setSelectedItem(rol);
        jComboBoxEstado.setSelectedItem(estado);
    }//GEN-LAST:event_jTable1MouseClicked

